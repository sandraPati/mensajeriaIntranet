package com.intranet.beans;

public class tipo_pano {
    Integer id;
    String nombre;

    public tipo_pano() {
    }

    public tipo_pano(Integer id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
